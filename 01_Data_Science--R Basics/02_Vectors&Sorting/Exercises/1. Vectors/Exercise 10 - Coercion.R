# Define the vector x
x <- c(1, 3, 5,"a")

# Note that the x is character vector
x

# Use `as.numeric` to coerce `x` to a numeric vector.
# Be sure to reassign the new vector as `x`.
x <- as.numeric(x)
x

# Use `as.character` to coerce `x` to a character vector.
# Be sure to reassign the new vector as `x`.
x <- as.character(x)
x